pub fn new_first_name<'a>() -> &'a str {
    random_name()
}

fn random_name<'a>() -> &'a str {
    let random_index = thread_rng().gen_range(0..FIRST_NAMES.len());
    FIRST_NAMES[random_index]
}

pub fn new_email_addr() -> String {
    let alphabet: Vec<char> = ('a'..'z').collect();
    let mut name_suffix = String::new();
    for _ in 0..6 {
        let letter = alphabet[thread_rng().gen_range(0..alphabet.len())];
        name_suffix.push(letter);
    }

    format!("{}.{}@gmail.com", random_name(), 123)
}
